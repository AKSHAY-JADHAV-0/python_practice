###################
#Newline
print("hi my name is akshay\nwhat is your name")

########
#tab
print("hi my name is akshay\twhat is your name")

################
#Form Feed-
print("hi my name is akshay \fwhat is your name")

############
#
#Backslash
print("C:\\Users\\Akshay")

#############
#Single Quote
print('hi my name is akshay, \'what\' is your name')

##################
#\0 – Null Character
print("Hello\0World!")

#################
#\a – Bell/Alert
print("Alert!\a")

##################
#\N{name} – Unicode Character by Name
print("hi my name is \n{Akshay}")
print("\N{GREEK CAPITAL LETTER DELTA}")

#####################
#\uXXXX – Unicode Character (16-bit hex)
print("\u03B1")
print("\U0001F600")
print("\x48\x65\x6C\x6C\x6F")
